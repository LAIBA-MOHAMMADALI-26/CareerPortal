<script>
  // JavaScript code to implement automatic image slider
  let currentSlide = 0;
  const slides = document.querySelectorAll(".slide1");

  function showSlide() {
      for (let i = 0; i < slides.length; i++) {
          slides[i].style.display = "none";
      }

      for (let i = currentSlide; i < currentSlide + 3; i++) {
          slides[i % slides.length].style.display = "block";
      }

      currentSlide = (currentSlide + 1) % slides.length;
  }

  function autoSlide() {
      showSlide();
      setTimeout(autoSlide, 2000); // 5000 milliseconds (2 seconds)
  }

  autoSlide();
  </script>


